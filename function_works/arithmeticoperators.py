# addition

def add(n1,n2):
    res=n1+n2
    return res
print(add(4,6))

# subtraction

def sub(n1,n2):
    res= n1-n2 if n1>n2 else n2-n1
    return res
print(sub(9,12))

# multiplication

def mult(n1,n2):
    res=n1*n2
    return res

print(mult(5,4))

# divison

def div(n1,n2):
    res=n1/n2
    return res
print(div(100,10))